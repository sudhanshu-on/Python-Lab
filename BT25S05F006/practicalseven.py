num1 = int(input("Enter first number: "))
num2 = int(input("Enter second number: "))

print("\nMaximum number:", max(num1, num2))
print("Minimum number:", min(num1, num2))

# absolute value
x = int(input("\nEnter a negative number: "))
print("Absolute value:", abs(x))

# power and round functions
a = float(input("\nEnter a number: "))
print("Square of number:", pow(a, 2))
print("Rounded value:", round(a, 2))

# type conversion
num = input("\nEnter a number in string form: ")
print("Converted to integer:", int(num))