# List methods
numbers = [10, 20, 30]
numbers.append(40)
numbers.insert(1, 15)
numbers.remove(30)
print("\nList after operations:", numbers)
# Tuple
t = (1, 2, 3, 4)
print("\nTuple:", t)
print("Count of 2:", t.count(2))
# Set 
s = {1, 2, 3}
s.add(4)
s.update([5, 6])
s.remove(2)
